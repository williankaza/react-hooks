import React, { useState } from 'react'

export default () =>{
    //setCounter = Função de manipulação (?)
    const [valor, setCounter] = useState(0);

    return (
        <div>
        <span>click x: {valor}</span>
        <button onClick={() => setCounter(valor + 1)}>Contar</button>
        </div>
    );
}










































































































































































